#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c[10];
	int		i;

	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	i = 0;
	if (n < 0)
	{
		write(fd, "-", 1);
		n = -n;
	}
	else if (n == 0)
		write(fd, "0", 1);
	while (n > 0)
	{
		c[i] = '0' + (n % 10);
		n /= 10;
		i++;
	}
	while (i-- > 0)
		write(fd, &c[i], 1);
}
